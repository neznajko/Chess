using std::endl;
using std::ostream;
////////////////////////////////////////////////////////
template <typename T>
std::ostream& operator<<( std::ostream& ostrm,
                          const std::vector<T>& vec ){
    ostrm << "[";
    for( std::size_t j{}, n{ vec.size()}; j < n; ++j ){
        ostrm << " " << vec[ j ] << " ,"[ j < n - 1 ];
    }
    return ostrm << "]";
}
