void benchmark( void (*f)()) {
    using namespace std::chrono;
    const auto start{ steady_clock::now()};
    f();
    const auto stop{ steady_clock::now()};
    duration<double> total = stop - start;
    std::cout << total.count() << " sec\n";
}
